package net.chenlin.dp.manage.program.entity;

public class ItemEntity {

    private String id;
    private Integer reality_height;
    private Integer reality_width;
    private Integer reality_x;
    private Integer reality_y;
    private Integer width;
    private Integer height;
    private Integer x;
    private Integer y;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Integer getReality_height() {
        return reality_height;
    }

    public void setReality_height(Integer reality_height) {
        this.reality_height = reality_height;
    }

    public Integer getReality_width() {
        return reality_width;
    }

    public void setReality_width(Integer reality_width) {
        this.reality_width = reality_width;
    }

    public Integer getReality_x() {
        return reality_x;
    }

    public void setReality_x(Integer reality_x) {
        this.reality_x = reality_x;
    }

    public Integer getReality_y() {
        return reality_y;
    }

    public void setReality_y(Integer reality_y) {
        this.reality_y = reality_y;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
